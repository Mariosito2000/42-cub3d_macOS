#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	unsigned int	num;
	int				sign;

	num = 0;
	sign = 1;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = (num * 10) + (str[i] - '0');
		i++;
	}
	if (num > 2147483647 && sign > 0)
		return (-1);
	else if (num > 2147483648 && sign < 0)
		return (0);
	return (sign * num);
}
